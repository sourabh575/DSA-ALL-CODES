#include<iostream>
using namespace std;
void print(int *arr,int s,int e){
	for(int i=s;i<=e;i++){
		cout<<arr[i]<<" ";
	}
	cout<<endl;
}

int firstoccurence(int *arr,int s,int e,int k){
	print(arr,s,e);
	int ans=-1;
	while(s<=e){
		int mid=(s+e)/2;
		if(arr[mid]==k){
			ans=mid;
			e=mid-1;
	    }
		else if(arr[mid]<k){
			return firstoccurence(arr,mid+1,e,k);
	    }
		else{
			return firstoccurence(arr,s,mid-1,k);
		}
	}
	return ans;
}


